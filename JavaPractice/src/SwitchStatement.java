
public class SwitchStatement {

	public static void main(String[] args) {
		
		double a=12.5;
		
		switch(a)
		{
		
		case 12 :
			System.out.println("Inside 1sts case");
		
		
		case 17:
			System.out.println("Inside 2nd case");
			
		case 2:
			System.out.println("Inside 3rd case");
			
			default :
			System.out.println("Inside default");
		}	
		
		

	}

}
